var storageWrapper={
	///Обёртка над хранилищем, в данном случае - GreaseMonkey
	getKey: function(key,defaultValue){
		try {
			var val = GM_getValue(key);
			if (val === undefined) {
				return defaultValue;
			} else {
				return JSON.parse(val);
			};
		} catch (e) {
			console.error(e);
			return defaultValue;
		}
	},
	setKey: function(key,value){
    try{
      GM_setValue(key,JSON.stringify(value));
    }catch(e){
      console.error(e);
    }
	},
};


